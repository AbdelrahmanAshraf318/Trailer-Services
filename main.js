$(document).ready(function() {
	// body...
	$('#services').on('click' , function() {

		
		$('.interface').hide();
		$('.paragraph').hide();
		$('.about').hide();
		$('.paragraph2').hide();
		$('.news').hide();
		$('.contact').hide();
		$('.contact2').show();
		$('.footer').show();
		$('#Servi').show();
		$('#background').css('background-image', 'url(trailer.jpg)');
		$('#news').hide();
		$('#faqs').hide();
		$('#aboutUs').hide();
		$('#back').hide();
		$('#contact').hide();
		

	});
});

